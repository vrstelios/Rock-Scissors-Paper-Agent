class ImageProcessingLayer(layers.Layer):
    def __init__(self, noise_std = 0.05, brightness_delta = 0.1, apply_filter = False):
        super(ImageProcessingLayer, self).__init__()
        self.noise_std = noise_std
        self.brightness_delta = brightness_delta
        self.apply_filter = apply_filter

    def call(self, inputs, training = None):
        # Apply image processing techniques to each input image
        x = inputs
        if training:
          # Horizontal flip
          x = tf.image.random_flip_left_right(x)
          # Brightness
          x = tf.image.random_brightness(x, max_delta = self.brightness_delta)
          # Gaussian noise
          noise = tf.random.normal(shape = tf.shape(x), mean = 0.0, stddev = self.noise_std, dtype = tf.float32)
          x = x + noise

        # Clip in [0,1]
        x = tf.clip_by_value(x, 0.0, 1.0)
        return x

    def get_config(self):
      return {'noise_std': self.noise_std, 'brightness_delta': self.brightness_delta, 'apply_filter': self.apply_filter}

